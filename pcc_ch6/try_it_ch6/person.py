# dictionary practice with my girlfriend as the subject
leah_beisel = {'first_name': 'leah',
               'last_name': 'beisel',
               'age': 29,
               'city': 'wrightwood'
               }

print(leah_beisel)
print(f'{leah_beisel['first_name'].title()} {leah_beisel['last_name'].title()} is my girlfriend.')
print(f'She is {leah_beisel['age']} and lives in {leah_beisel['city'].title()}.')