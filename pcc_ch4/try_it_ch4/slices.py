# assorted slicing practice
new_list = [1, 20, 100, 25, 12]

# first three items in the list
print("The first three items are")
print(new_list[0:3])

# middle three items in the list
print("The middle three items are")
print(new_list[1:4])

# last three items in the list
# new_list[2:5] would work, too
print("The last three items are")
print(new_list[-3:])