# if/elif/else determining a person's stage of life
age = 30

if age < 2:
    print("Ya just a bb!")
elif age >= 2 and age < 4:
    print("Hello, toddler!")
elif age >= 4 and age < 13:
    print("Get outta here, kid!")
elif age >= 13 and age < 20:
    print("Eww... a teenager.")
elif age >= 20 and age < 65:
    print("Welcome to adulthood, pleb!")
elif age >= 65:
    print("Respect yer elders!")